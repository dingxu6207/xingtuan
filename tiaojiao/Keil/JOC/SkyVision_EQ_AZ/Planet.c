void calRaDec(double L,double bta,double e,double *mra0,double*mdec0)
{	 
	    double pi,afa,dta;
		int Lq;

		pi=3.1415927;

		L=L*pi/180.0;
		bta=bta*pi/180.0;///3600.0;
		e=e*pi/180.0;

		afa=atan((sin(L)*cos(e)-tan(bta)*sin(e))/cos(L));
		afa=afa*180/pi;
		dta=asin(sin(bta)*cos(e)+cos(bta)*sin(e)*sin(L));
		dta=dta*180/pi;
		
		L=L*180/pi;
		if((L>0)&&(L<=90)) 
		{
			Lq=1;
		}
		if ((L>90)&&(L<=180))
		{
			Lq=2;
		}
		if ((L>180)&&(L<=270))
		{
			Lq=3;
		}
		if ((L>270)&&(L<=360))
		{
			Lq=4;
		}
		
		switch (Lq) 
		{		
		case 1: 
			if(afa<0)
			{
				afa=-1*afa;
			}
			break;
			
		case 2:
			if(afa>=0)
			{
				afa=180-afa;
			}
			else
			{
				afa=180+afa;
			}
			break;
		case 3:
			if(afa>=0)
			{
				afa=180+afa;
			}
			else
			{
				afa=180-afa;
			}
			break;
		case 4:
			
			if(afa>=0)
			{
				afa=360-afa;
			}
			else
			{
				afa=360+afa;
			}
			break;
		default:
			break;
			
		}
		afa=afa/15.0; 
	   
        *mra0=afa;
		*mdec0=dta;
}
