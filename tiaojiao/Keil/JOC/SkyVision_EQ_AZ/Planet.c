/******************************************************************************
 *函数：angleConvert(double angle)
 *功能：将输入的角度值angle调整到0~360度的范围之内。
 *输入：double angle --需要调整的角度
 *输出：double angle --调整之后的角度值
 ******************************************************************************/
double angleConvert(double angle){
	while(1){
		if(angle>360){
			angle -=360;
		}
		else if(angle<0){
			angle +=360;
		}
		else break;
	}
	return angle;
}

